        public static void main(String[] args) {
            int a, b, c;
            a = 10;
            System.out.println("a is " + a);
            b = c = a;
            System.out.println("b is " + b);
            System.out.println("c is " + c);
            b += 5;
            System.out.println("b is " + b);
            int x = 10, y = 2;
            int z = (x > y) ? x : y; // 10
            System.out.println("x is " + x);
            System.out.println("y is " + y);
            System.out.println("z is " + z);

        }

