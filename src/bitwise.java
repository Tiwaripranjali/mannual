//Bitwise Operator
public class bitwise {
    public static void main(String[] args) {
        int a=9,b=10;
        int c=a&b;
        int d=a|b;
        int e=a^b;
        int f=~a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        int v1=78;
        int x=v1<<3;
        int y=v1>>4;
        System.out.println(x);
        System.out.println(y);
    }
}
